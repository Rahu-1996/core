
public class JavaLangMathClass {
	
	public static void main (String[]args) {
		
		System.out.println("Max 10,25="+Math.max(10,25));
		
		System.out.println("Min 10,25="+Math.min(10,25));
		
		System.out.println("Absolute 4.8,="+Math.abs(4.8));
		
		System.out.println("Exp10="+Math.exp(10));
		
		System.out.println("Random Number="+Math.random());
		
		System.out.println("Square Root="+Math.sqrt(4));
		
		
	}

}
